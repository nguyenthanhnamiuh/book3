    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        String oldPassword = new String(pwdOldPassword.getPassword());
        String newPassword = new String(pwdNewPassword.getPassword());
        String retypePassword = new String(pwdRetypePassword.getPassword());
        if (oldPassword.length() < 6) {
            ErrorMessage.showMessageWithFocusTextField("Lỗi", "Mật khẩu cũ phải tối thiểu 6 ký tự!", pwdOldPassword);
            return;
        }

        if (newPassword.length() < 6) {
            ErrorMessage.showMessageWithFocusTextField("Lỗi", "Mật khẩu mới phải tối thiểu 6 ký tự!", pwdNewPassword);
            return;
        }
        if (retypePassword.length() < 6) {
            ErrorMessage.showMessageWithFocusTextField("Lỗi", "Mật khẩu nhập lại phải tối thiểu 6 ký tự!", pwdRetypePassword);
            return;
        }

        if (!newPassword.equals(retypePassword)) {
            ErrorMessage.showMessageWithFocusTextField("Lỗi", "Mật khẩu mới và mật khẩu nhập lại không trùng khớp", pwdRetypePassword);
            return;
        }

        if (!CurrentSession.getNhanVien().getPassword().equals(oldPassword)) {
            ErrorMessage.showMessageWithFocusTextField("Lỗi", "Mật khẩu cũ sai! Hãy kiểm tra lại.", pwdOldPassword);
            return;
        }

        NhanVien_BUS nhanVien_BUS = new NhanVien_BUS();
        boolean isSuccess = nhanVien_BUS.resetUserPassword(CurrentSession.getNhanVien().getUserName(), newPassword);
        if (isSuccess) {
            ErrorMessage.showMessageWithFocusTextField("Thông tin", "Đã đổi mật khẩu thành công!", pwdOldPassword);
            pwdNewPassword.setText("");
            pwdOldPassword.setText("");
            pwdRetypePassword.setText("");
        } else {
            ErrorMessage.showMessageWithFocusTextField("Thông tin", "Đổi mật khẩu thất bại! Đã có lỗi với CSDL.", pwdOldPassword);

        }
    }//GEN-LAST:event_btnConfirmActionPerformed

